function kleinsteGetal(getal1,getal2,getal3,getal4) {
    let kleinsteGetal = Math.min(getal1,getal2,getal3,getal4);
    console.log(kleinsteGetal);
}
function grootsteGetal(getal1,getal2,getal3,getal4) {
    let grootsteGetal = Math.max(getal1,getal2,getal3,getal4);
    console.log(grootsteGetal);
}






kleinsteGetal(5,9,2,6);
grootsteGetal(5,9,2,6);